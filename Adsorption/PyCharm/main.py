# ==============================================================================================================
# Import all needed Calculators
# ==============================================================================================================
from ase.build import fcc111, add_adsorbate
from ase.io import write
from ase.visualize import view
# ==============================================================================================================

# ==============================================================================================================
# Loop to set up CO2 adsorbed on Cu111 in the ontop, bridge, and fcc positions
# ==============================================================================================================
position={'ontop', 'bridge', 'fcc'}
for i in position:
    # ==========================================================================================================
    # Set up Cu111 with size=(2x2x4)
    # ==========================================================================================================
    slab = fcc111('Cu', size=(2, 2, 4), vacuum=20.0)
    slab.center(axis=2, vacuum=0.0)
    view(slab)
    write("Cu111.in", slab)
    # ==========================================================================================================
    add_adsorbate(slab, 'O', height=2.00000, position=i, offset=None, mol_index=0)
    add_adsorbate(slab, 'C', height=3.17069, position=i, offset=None, mol_index=0)
    add_adsorbate(slab, 'O', height=4.34138, position=i, offset=None, mol_index=0)
    view(slab)
    if i is 'ontop':
        filename="CO2Cu111.ontop.in"
    elif i is 'bridge':
        filename="CO2Cu111.bridge.in"
    elif i is 'fcc':
        filename="CO2Cu111.fcc.in"
    else:
        filename=0
    write(filename, slab)
# ==============================================================================================================
